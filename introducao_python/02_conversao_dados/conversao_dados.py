# Conversão de Dados

# 1. Conversão de tipos de dados
num_int = 10
print(num_int)
print(type(num_int))
print('------------------------------------------------------')
# aqui convertemos o tipo de dado de int para float
novo_num_float = float(num_int)
print(novo_num_float)
print(type(novo_num_float))
print('------------------------------------------------------')

# aqui convertemos o tipo de dado de float para string
novo_num_str = str(novo_num_float)
print(novo_num_str)
print(type(novo_num_str))
print('------------------------------------------------------')
